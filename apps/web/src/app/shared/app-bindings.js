import { createButtonBindingHelper } from "./button-binding.js";
import {
  bindConfirmModalButtons,
  bindDuplicateModalButtons,
  bindOnboardingButtons,
  bindShortcutsModalButtons,
  bindVoiceHistoryButtons,
} from "./app-bindings-modals.js";
import { bindSearchButtons } from "./app-bindings-search.js";
import {
  bindAdminTelemetryButtons,
  bindHealthStorageButtons,
} from "./app-bindings-admin.js";
import { bindPairedActionButtons } from "./app-bindings-actions.js";
import { bindCoreInputBindings } from "./app-bindings-core.js";
import {
  bindChatListButtons,
  bindStatusRetryButton,
} from "./app-bindings-runtime.js";
import { bindThemeButton } from "./app-bindings-theme.js";

export function createAppBindingsController({
  state,
  elements,
  controllers,
  modalControls,
  actions,
}) {
  const buttonBinding = createButtonBindingHelper();
  const {
    inputEl,
    sendBtnEl,
    tabsEl,
    duplicateModalEl,
    duplicateTitleInputEl,
    duplicateModeUserEl,
    duplicateCancelBtnEl,
    duplicateConfirmBtnEl,
    confirmModalEl,
    confirmCancelBtnEl,
    confirmOkBtnEl,
    voiceHistoryModalEl,
    voiceHistoryBtnEl,
    voiceHistoryCloseBtnEl,
    clearVoiceHistoryBtnEl,
    shortcutsModalEl,
    shortcutsHelpBtnEl,
    shortcutsCloseBtnEl,
    onboardingModalEl,
    onboardingBtnEl,
    onboardingRunChecksBtnEl,
    onboardingSkipBtnEl,
    onboardingCompleteBtnEl,
    newChatBtnEl,
    newChatBtnMobileEl,
    renameBtnEl,
    renameBtnMobileEl,
    favoriteBtnEl,
    archiveBtnEl,
    tagsBtnEl,
    systemPromptBtnEl,
    duplicateBtnEl,
    duplicateBtnMobileEl,
    deleteBtnEl,
    deleteBtnMobileEl,
    exportBtnEl,
    exportBtnMobileEl,
    exportJsonBtnEl,
    exportJsonBtnMobileEl,
    exportAllJsonBtnEl,
    exportAllJsonBtnMobileEl,
    exportFavoritesMdBtnEl,
    exportFavoritesMdBtnMobileEl,
    importJsonBtnEl,
    importJsonBtnMobileEl,
    backupBtnEl,
    backupBtnMobileEl,
    restoreBackupBtnEl,
    restoreBackupBtnMobileEl,
    darkModeBtnEl,
    statusRetryBtnEl,
    searchBtnEl,
    searchInputEl,
    searchClearBtnEl,
    searchPrevBtnEl,
    searchNextBtnEl,
    searchRoleEl,
    searchFromEl,
    searchToEl,
    chatListLoadMoreBtnEl,
    telemetryOptInEl,
    telemetryStatsBtnEl,
    auditExportBtnEl,
    configHistoryBtnEl,
    diagnosticsExportBtnEl,
    healthRefreshBtnEl,
    storageRefreshBtnEl,
    storageCleanupBtnEl,
    storageLimitBtnEl,
  } = elements;
  const {
    shortcutsController,
    onboardingController,
    voiceController,
    statusPresenter,
  } = controllers;
  const {
    closeDuplicateModal,
    closeConfirmModal,
    closeShortcutsModal,
    closeVoiceHistoryModal,
    openConfirmModal,
    openShortcutsModal,
    showStatus,
  } = modalControls;
  const {
    enviar,
    createNewChat,
    renameActiveChat,
    toggleFavoriteActiveChat,
    toggleArchiveActiveChat,
    editTagsActiveChat,
    editChatSystemPrompt,
    duplicateActiveChat,
    deleteActiveChat,
    exportChat,
    exportChatJson,
    exportAllChatsJson,
    exportFavoriteChatsMarkdown,
    importChatJson,
    exportFullBackup,
    restoreFullBackup,
    openVoiceHistoryModalWithRender,
    cycleThemeMode,
    saveThemeForCurrentUser,
    openOnboardingModal,
    closeOnboardingModal,
    runOnboardingChecks,
    checkOllamaStatus,
    loadStorageUsage,
    runStorageCleanup,
    updateStorageLimitForCurrentUser,
    setTelemetryEnabled,
    showTelemetryStats,
    exportAuditLogsJson,
    openConfigHistoryRollback,
    exportDiagnosticsPackage,
    loadChats,
    runHistorySearch,
    clearSearchResults,
  } = actions;

  function updateSendButtonState() {
    sendBtnEl.disabled = inputEl.value.trim() === "";
  }

  function bindAll() {
    shortcutsController.renderShortcutsHelp();
    bindCoreInputBindings({
      inputEl,
      duplicateModalEl,
      confirmModalEl,
      shortcutsModalEl,
      voiceHistoryModalEl,
      onboardingModalEl,
      closeDuplicateModal,
      closeConfirmModal,
      closeShortcutsModal,
      closeVoiceHistoryModal,
      onboardingController,
      shortcutsController,
      updateSendButtonState,
      enviar,
    });
    bindDuplicateModalButtons({
      duplicateCancelBtnEl,
      duplicateConfirmBtnEl,
      duplicateTitleInputEl,
      duplicateModeUserEl,
      closeDuplicateModal,
    });
    bindPairedActionButtons({
      buttonBinding,
      newChatBtnEl,
      newChatBtnMobileEl,
      renameBtnEl,
      renameBtnMobileEl,
      favoriteBtnEl,
      archiveBtnEl,
      tagsBtnEl,
      systemPromptBtnEl,
      duplicateBtnEl,
      duplicateBtnMobileEl,
      deleteBtnEl,
      deleteBtnMobileEl,
      exportBtnEl,
      exportBtnMobileEl,
      exportJsonBtnEl,
      exportJsonBtnMobileEl,
      exportAllJsonBtnEl,
      exportAllJsonBtnMobileEl,
      exportFavoritesMdBtnEl,
      exportFavoritesMdBtnMobileEl,
      importJsonBtnEl,
      importJsonBtnMobileEl,
      backupBtnEl,
      backupBtnMobileEl,
      restoreBackupBtnEl,
      restoreBackupBtnMobileEl,
      createNewChat,
      renameActiveChat,
      toggleFavoriteActiveChat,
      toggleArchiveActiveChat,
      editTagsActiveChat,
      editChatSystemPrompt,
      duplicateActiveChat,
      deleteActiveChat,
      exportChat,
      exportChatJson,
      exportAllChatsJson,
      exportFavoriteChatsMarkdown,
      importChatJson,
      exportFullBackup,
      restoreFullBackup,
    });
    bindVoiceHistoryButtons({
      voiceHistoryBtnEl,
      voiceHistoryCloseBtnEl,
      clearVoiceHistoryBtnEl,
      openVoiceHistoryModalWithRender,
      closeVoiceHistoryModal,
      openConfirmModal,
      voiceController,
    });
    bindConfirmModalButtons({
      confirmCancelBtnEl,
      confirmOkBtnEl,
      closeConfirmModal,
    });
    bindThemeButton({
      darkModeBtnEl,
      cycleThemeMode,
      saveThemeForCurrentUser,
      showStatus,
    });
    bindOnboardingButtons({
      onboardingBtnEl,
      onboardingRunChecksBtnEl,
      onboardingSkipBtnEl,
      onboardingCompleteBtnEl,
      openOnboardingModal,
      runOnboardingChecks,
      closeOnboardingModal,
      onboardingController,
      showStatus,
    });
    bindShortcutsModalButtons({
      shortcutsHelpBtnEl,
      shortcutsCloseBtnEl,
      openShortcutsModal,
      closeShortcutsModal,
    });
    bindStatusRetryButton({
      statusRetryBtnEl,
      statusPresenter,
    });
    bindSearchButtons({
      state,
      searchBtnEl,
      searchInputEl,
      searchClearBtnEl,
      searchPrevBtnEl,
      searchNextBtnEl,
      searchRoleEl,
      searchFromEl,
      searchToEl,
      runHistorySearch,
      clearSearchResults,
    });
    bindChatListButtons({
      state,
      tabsEl,
      chatListLoadMoreBtnEl,
      loadChats,
    });
    bindAdminTelemetryButtons({
      telemetryOptInEl,
      telemetryStatsBtnEl,
      auditExportBtnEl,
      configHistoryBtnEl,
      diagnosticsExportBtnEl,
      setTelemetryEnabled,
      showTelemetryStats,
      exportAuditLogsJson,
      openConfigHistoryRollback,
      exportDiagnosticsPackage,
      showStatus,
    });
    bindHealthStorageButtons({
      state,
      healthRefreshBtnEl,
      storageRefreshBtnEl,
      storageCleanupBtnEl,
      storageLimitBtnEl,
      checkOllamaStatus,
      loadStorageUsage,
      runStorageCleanup,
      updateStorageLimitForCurrentUser,
      showStatus,
    });
  }

  return {
    bindAll,
    updateSendButtonState,
  };
}